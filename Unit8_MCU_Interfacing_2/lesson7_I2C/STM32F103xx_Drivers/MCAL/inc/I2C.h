typedef struct {
	uint16_t Enable_Dual_Address ;
	uint16_t primary_address ;
	uint16_t secondary_address ;
	uint32_t addressing_slave_mode ; 	// This parameter is set from @ref I2C_Slave_Address_Mode @ I2C.h
}I2C_Slave_Device_Address;

typedef enum {
	I2C_EV_DATA_REQ ,
	I2C_EV_DATA_REC
}EN_Slave_State;

typedef enum {
	without_stop ,
	with_stop
}EN_Stop_Condition;

typedef enum {
	repeated_start ,
	non_repeated_start
}EN_Start;

typedef enum {
	disable,
	enable
}EN_Functional_state;

typedef enum {
	Reset = 0 ,
	Set = 1
}EN_Flag_Status;

typedef enum {
	I2C_Flag_Busy ,
	EV5 ,
	EV6 ,
	EV7 ,
	EV8 ,
	Master_Transmitter_Event = ((uint32_t)(0x00070080))		//check if BUSY,TRA,MSL,TXE flags are all set
}EN_Status;

typedef enum {
	Transmit = 0 ,
	Receive
}EN_Operation;

typedef struct {
	// specifies the clock speed  Standard Speed (up to 100 kHz) â€“ Fast Speed (up to 400 kHz)
	// This parameter is set from @ref I2C_CLK_SPEED @ I2C.h
	uint32_t clock_speed ;

	// specifies clock stretching mode
	// This parameter is set from @ref I2C_CLK_STRETCH @ I2C.h
	uint32_t clock_stretch_mode ;

	// specifies I2C mode (I2C / SMBus)
	// This parameter is set from @ref I2C_MODE @ I2C.h
	uint32_t I2C_mode ;

	// specifies I2C Device Address
	// This parameter is set from @ref I2C_Slave_Address_Mode
	I2C_Slave_Device_Address slave_device_address ;

	// specifies Acknowledge control (Enable / Disable)
	// This parameter is set from @ref I2C_ACK_Control @ I2C.h
	uint32_t Ack_control ;

	// slave interrupt callback
	void(*p_slave_callback)(EN_Slave_State state) ;


}I2C_Config_t;


// ---------------------------------Reference MACROS ------------------------------------------------
// @ref I2C_CLK_SPEED
#define I2C_CLK_SM_50k	(50000U)
#define I2C_CLK_SM_100k	(100000U)
#define I2C_CLK_FM_200k	(200000U)
#define I2C_CLK_FM_400k	(400000U)
// ==================================================

// @ref I2C_CLK_STRETCH
/*
 * This bit is used to disable clock stretching in slave mode when ADDR or BTF flag is set, until it is reset by software.
	0: Clock stretching enabled
	1: Clock stretching disabled
 *
 */
#define I2C_CLK_STRETCH_ENABLE	(0x00000000U)
#define I2C_CLK_STRETCH_DISABLE	I2C_CR1_NOSTRETCH
// ==================================================

// @ref I2C_MODE
/*
 *  SMBUS: SMBus mode
	0: I2C mode
	1: SMBus mode
 */
#define I2C_MODE_I2C	(0x00000000UL)
#define I2C_MODE_SMBUS	I2C_CR1_SMBUS
// ==================================================

// @ref I2C_Slave_Address_Mode
/*
 * Bit 15 ADDMODE in I2C_OAR1 Addressing mode (slave mode)
	0: 7-bit slave address (10-bit address not acknowledged)
	1: 10-bit slave address (7-bit address not acknowledged)
 */
#define I2C_Slave_Address_Mode_7bits	(0x00000000U)
#define I2C_Slave_Address_Mode_10bits	(uint16_t)(1 << 15)
// ==================================================

// @ref I2C_ACK_Control
/*
 * Bit 10 ACK: Acknowledge enable in I2C_CR1
	This bit is set and cleared by software and cleared by hardware when PE=0.
	0: No acknowledge returned
	1: Acknowledge returned after a byte is received (matched address or data)
 */
#define I2C_Ack_DISABLE	(0x00000000U)
#define I2C_Ack_ENABLE 	I2C_CR1_ACK
// ==================================================


